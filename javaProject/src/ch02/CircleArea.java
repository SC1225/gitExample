package ch02;

public class CircleArea {

	public static void main(String[] args) {
		double PI = 3.14;
		double radius = 10.0;
		double area = radius * radius * PI;
		System.out.println("===========================");
		System.out.println("원의 면적은 " + area + " 입니다.");
		System.out.println("===========================");


	}

}
